def build_messages(
    title: str, content: str, existing_case_names: list[str] | None = None
) -> list[dict]:
    """LLM에 보낼 메시지 리스트 구성"""
    # 본문 3000자 제한 (토큰 절약)
    truncated = content[:3000] if len(content) > 3000 else content

    # 기존 사건 그룹 목록을 시스템 프롬프트에 추가
    system_content = SYSTEM_PROMPT
    if existing_case_names:
        case_list = "\n".join(f"- {name}" for name in existing_case_names)
        system_content += f"\n\n## 기존 사건 그룹 목록\n{case_list}"

    user_message = f"""[기사 분석 요청]
제목: {title}
본문: {truncated}"""

    messages = [
        {"role": "system", "content": system_content},
        *FEW_SHOT_EXAMPLES,
        {"role": "user", "content": user_message},
    ]
    return messages

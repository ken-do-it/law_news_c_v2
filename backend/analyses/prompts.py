def build_messages(title: str, content: str) -> list[dict]:
    """LLM에 보낼 메시지 리스트 구성"""
    # 본문 3000자 제한 (토큰 절약)
    truncated = content[:3000] if len(content) > 3000 else content

    user_message = f"""[기사 분석 요청]
제목: {title}
본문: {truncated}"""

    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        *FEW_SHOT_EXAMPLES,
        {"role": "user", "content": user_message},
    ]
    return messages
